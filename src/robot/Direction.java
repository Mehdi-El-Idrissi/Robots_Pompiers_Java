package robot;

/**
 * Enumération représentant les quatre directions cardinales.
 * Chaque direction correspond à un point de la boussole par rapport à la carte.
 */
public enum Direction {

    /**
     * Direction vers le nord (haut de la carte).
     */
    NORD, 

    /**
     * Direction vers le sud (bas de la carte).
     */
    SUD,  

    /**
     * Direction vers l'est (droite de la carte).
     */
    EST,  

    /**
     * Direction vers l'ouest (gauche de la carte).
     */
    OUEST 
}
